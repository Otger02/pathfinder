/**
 * summary-generator.ts
 *
 * Generates a Pathfinder summary PDF from personal data and authorization info.
 * Uses pdf-lib to create the PDF from scratch (no template needed).
 */

import { PDFDocument, StandardFonts, rgb, PDFFont, PDFPage } from "pdf-lib";
import type { PersonalData } from "@/lib/types/personal-data";

interface AuthInfo {
  slug: string;
  name: string;
  description: string;
  requirements: string[];
  steps: string[];
  laws: string[];
  exForms: string[];
  deadline: string | null;
}

interface SummaryOptions {
  personalData: PersonalData;
  authorization: AuthInfo;
  lang: string;
}

// ── Constants ───────────────────────────────────────────────────

